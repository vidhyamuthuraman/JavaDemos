package OOP;

public class Encapsulation {

	
	private String name;
	private int age,rollno;
	
	public String getname()
	{
		return name;
	}
	public void setname(String n)
	{
		name=n;
		
	}
	public int getage()
	{
		return age;
	}
	public void setage(int a)
	{
		age=a;
	}
	public int getrollno()
	{
		return rollno;
	}
	public void setrollno(int r )
	{
		rollno=r;
	}


}
